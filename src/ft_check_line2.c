#include "../include/cub3d.h"

int	ft_check_line2(char *str)
{
	int	i;

	i = -1;
	while (str[++i] && str[i] != ' ')
		;
	--i;
	while (str[++i] && str[i] == ' ')
		;
	if (str[i] != '\0')
		return (1);
	return (0);
}
